/// `slot_input::SlotSubkey::is_secret_bearing` at
/// `crates/mnemonic-toolkit/src/slot_input.rs`.
pub const SECRET_SLOT_SUBKEYS: &[&str] = &["phrase", "seedqr", "entropy", "xprv", "wif"];
